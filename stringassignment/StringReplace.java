package com.stringassignment.util;

public class StringReplace {

	public static void main(String[] args) {
		System.out.println("\n\tReplace a Blank Space to %20");
		String str = "This is a String Operation";
		System.out.println("\nGiven String : "+str);
		System.out.println("\nReplace blank space to %20 : "+str.replace(" ","%20"));
	
	}
	
}
