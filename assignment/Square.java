package com.assignment.util;

public class Square {

	public static void main(String[] args) {
		System.out.println("\tFirst 10 numbers with their square values");
		System.out.println("\nOutput:\n\t");
		for(int i=1;i<=10;i++) {
			int s = i * i;
			System.out.println(+i+" "+s);
		}

	}

}
